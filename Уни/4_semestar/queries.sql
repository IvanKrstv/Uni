-- Тьреене на полети по: дестинация, дата, авиокомпания.
SELECT
    f.FLIGHT_ID,
    f.FLIGHT_DATE,
    d_from.CITY_NAME,
    d_to.CITY_NAME,
    f.PRICE,
    m.MODEL_NAME,
    c.COMPANY_NAME
FROM
    FLIGHT f
JOIN
    DESTINATION d_from
ON
    f.TO_DESTINATION_ID = d_from.DESTINATION_ID
JOIN
    DESTINATION d_to
ON
    f.FROM_DESTINATION_ID = d_to.DESTINATION_ID
JOIN
    PLANE p
ON
    p.PLANE_ID = f.PLANE_ID
JOIN
    MODEL m
ON
    m.MODEL_ID = p.MODEL_ID
JOIN
    Company c
ON
    c.COMPANY_ID = m.COMPANY_ID
WHERE
    --d_to.city_name = 'London'
    --f.flight_date = ''
    c.COMPANY_NAME = 'Boeing'
;


